#include "stackNode.h"

stackNode::stackNode(string newDate)
{
	date = newDate;
	mpNext = nullptr;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////
string stackNode::getDate() const
{
	return (this->date);
}

stackNode * stackNode::getNext() const
{
	return (this->mpNext);
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////////
void stackNode::setDate(string newDate)
{
	date = newDate;
}

void stackNode::setNext(stackNode *pNewNext)
{
	mpNext = pNewNext;
}
///////////////////////////////////////////////////////////////////////////////////////////////////////////